// Developer Search
let devSearch = document.getElementById('developer-search');
let memberContainer = document.getElementById('members');
let searchResultsContainer = document.getElementById('search-results');

let completeMemberList = [];
for(let i = 0; i<members.length; i++) {
  completeMemberList = [...completeMemberList, ...members[i]];
} // Adding all members to a common list, completeMemberList
completeMemberList.sort((a, b) => {
  return (a.name.firstName + " " + a.name.lastName).localeCompare(b.name.firstName + " " + b.name.lastName);
}); //Lexicographic sorting of names

let searchValue = "";
let searchResults = [];
devSearch.addEventListener('keyup', (e) => {
  searchValue = e.target.value;
  
  //Display Containers
  if(searchValue == "") {
    searchResultsContainer.style.display = "none";
    memberContainer.style.display = "block";
    return;
  } else {
    searchResultsContainer.style.display = "flex";
    memberContainer.style.display = "none";
  }
  
  // Finding Search Results
  let searchRE = new RegExp(searchValue, "i");
  searchResults = completeMemberList.filter(member => searchRE.test(member.name.firstName + " " + member.name.lastName))

  //Inserting cards
  searchResultsContainer.innerHTML = "";
  if(searchResults.length != 0) {
    for(let i = 0; i<searchResults.length; i++) {
      searchResultsContainer.innerHTML += memberCard(searchResults[i]);
    }
  } else {
    searchResultsContainer.innerHTML = "No results matched"
  }


  setDimensions();

  
  const memberCards = document.getElementsByClassName("member");
  [...memberCards].forEach((project) => {
    observer.observe(project);
  });
})

